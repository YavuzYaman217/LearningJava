public class Pizza {

    String bread = "Null";
    String sauce = "Null";
    String cheese = "Null";
    String topping = "Null";

    Pizza(String bread, String sauce) {
        this.bread = bread;
        this.sauce = sauce;
    }

   Pizza(String bread, String sauce, String cheese, String topping) {
       this.bread = bread;
       this.sauce = sauce;
       this.cheese = cheese;
       this.topping = topping;

   }


}
